package sig.oss;

public class Calc {
    public int add(int x, int y) {
        System.out.print(">>>>>>> Run add method ....");
        return x+y;
    }

    public int sub(int x, int y) {
        System.out.print(">>>>>>> Run sub method ...");
        return x - y;
    }

    public int mul(int x, int y) {
        System.out.print(">>>>>>> Run sub method ...");
        return x * y;
    }
}